  <h2>Privacy Policy</h2>
  <p><strong>Last Updated:</strong> November 3, 2025</p>
  
  <p>Łuna ("we," "our," or "us") provides an alternative interface to Home Access Center (HAC) for accessing academic information. This Privacy Policy outlines our practices regarding the collection, use, and protection of your information.</p>

  <h3 class="mt-4">Age Restrictions and Verification</h3>
  <p>In compliance with applicable privacy regulations, access to Łuna is restricted to individuals thirteen years of age or older. Our system automatically verifies user age through date of birth information obtained directly from Home Access Center during the authentication process.</p>
  
  <p>Users who do not meet the minimum age requirement are immediately prevented from accessing the service. No data processing, caching, or storage occurs for underage users. Age verification is performed during each login session, and the date of birth data used for verification is promptly discarded after validation.</p>

  <h3 class="mt-4">Parental Exceptions</h3>
  <p>In limited circumstances, we may permit access for a user under the age of 13 when a parent or legal guardian has provided explicit consent for that child to use Łuna. Such consent must be obtained and verified directly by the parent or guardian before access is granted. Łuna does not accept or respond to general parental-consent requests or emails; these exceptions are granted only on a private, pre-approved basis. Data for these users is processed and displayed in the same secure, non-retentive manner described in this policy.</p>

  <h3 class="mt-4">Information Collection</h3>
  <p>We collect limited information necessary to provide our services:</p>
  <ul>
    <li><strong>HAC Authentication Credentials</strong> - Provided during login and maintained exclusively within your browser session. These credentials are never stored on our infrastructure and are cleared upon session termination.</li>
    <li><strong>Academic Data from HAC</strong> - Including student identification, course information, academic performance records, attendance data, and scheduling information. Date of birth is collected solely for age verification purposes.</li>
    <li><strong>User Configuration Preferences</strong> - Settings related to advertising preferences, notification systems, and academic performance tools.</li>
  </ul>
  <p>As an independent service, Łuna retrieves academic data directly from publicly available or user-authenticated Home Access Center endpoints. We do not operate on behalf of, or under the direction of, any educational institution.</p>

  <h3 class="mt-4">Regulatory Compliance</h3>
  <p>Łuna complies with applicable privacy laws, including the Children’s Online Privacy Protection Act (COPPA). Our age-verification mechanism ensures that no information is collected from users under thirteen (13) years of age. Because Łuna operates independently of schools and districts, the Family Educational Rights and Privacy Act (FERPA) does not apply to our operations.</p>
  <p>Łuna is an independent project created by a student and is not affiliated with, endorsed by, or operated on behalf of any school district or educational institution. Because Łuna does not act as a service provider to any school and has no formal agreement or partnership with any educational entity, it is not subject to the Family Educational Rights and Privacy Act (FERPA).</p>

  <p>Users access their own Home Access Center data voluntarily using their credentials. Łuna temporarily caches this information to provide enhanced features such as grade prediction, report card and interim progress report viewing, and user-created calendar tools. Cached data is used exclusively to power these in-app functions and is automatically purged on the first day of each month in accordance with the Data Retention policy.</p>

  <h3 class="mt-4">Analytics and Tracking</h3>
  <p>Łuna does not employ any analytics platforms, tracking pixels, or user monitoring services. We do not utilize services such as Google Analytics or similar tracking technologies.</p>

  <h3 class="mt-4">Use of Information</h3>
  <p>Collected information is used exclusively for the following purposes:</p>
  <ul>
    <li>Presentation of academic data through our enhanced interface</li>
    <li>Age verification to ensure regulatory compliance</li>
    <li>Academic performance predictions based on historical data</li>
    <li>Delivery of educational notifications and alerts</li>
    <li>Temporary caching of HAC data to optimize service performance</li>
  </ul>

  <h3 class="mt-4">Data Retention and Storage</h3>
  <p>We maintain minimal data retention practices:</p>
  <ul>
    <li>Authentication credentials persist only within active browser sessions</li>
    <li>Cached academic information is automatically purged on the first day of each month</li>
    <li>Date of birth data is utilized exclusively for real-time age verification and is not retained</li>
    <li>We do not engage in the sale, rental, or commercial distribution of user data</li>
  </ul>

